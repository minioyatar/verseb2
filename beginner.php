<p><strong>PART A – READING MATERIAL</strong></p>
	<ul style="list-style-type:roman">
		<li><p><span class="blueprint">SELF-HELP BOOKS</span> – a complete list of self-help books currently available on Shakespeare speaking / performance. &nbsp; This section will be principally of interest to actors. &nbsp; At a later stage we include some sort of rating guidance. &nbsp; Nearly every book has some merit, but, as to be expected, some are more sound than others. &nbsp; Moreover, some are more suitable to beginners, others for intermediate / advanced. </p></li>
		<li><p><span class="blueprint">ANNOTATIONS</span> – a complete list of sources for the annotations (definitions and comment). &nbsp; Note that the annotations in some editions are hopelessly inadequate for performance, whilst some more advanced editions suffer from being too academic and literary. &nbsp; Out annotations are entirely performance driven, and therefore every annotation counts for the interested actor / director.</p></li>
		<li><p><span class="blueprint">ACADEMIC REFERENCES</span> – a complete list of a wide variety of sources we have consulted over the years to help us improve our overall technique and understanding when editing and commenting on Shakespeare in our Quarto, First Folio and modern ePlays. &nbsp; This list is constantly under review and each new edition of the ePlays will incorporate fresh readings and improvements.</p></li>
		<li><p><span class="blueprint">EDITIONS OF SHAKESPEARE</span> – a complete list of all the available editions of Shakespeare that at one time or another we have looked at or consulted. &nbsp; Though the scholarship is often excellent most of these are geared to the schoolroom or literary market; disappointingly few are geared to actors. &nbsp; Even when the text is good the annotations focus excessively on academic and literary points of order at the expense of performance information. &nbsp; It was for this reason Versebuster was born. &nbsp; The Cambridge School series is a peculiar hybrid in that it often has lame and imprecise definitions but compensates with thoughtful performance commentary lacking in other editions. </p></li>
	</ul>

<br>
<p><strong>PART B – AUDIO & VISUAL</strong></p>
	<ul start="5" style="list-style-type:roman; ">
		<li><p><span class="blueprint">LECTURES</span> – there are many excellent lectures that can be mined for a better general understanding of Shakespeare  and performance ideas. &nbsp; The advantage of lectures for some will be the medium. &nbsp; It’s simply more entertaining to watch a 50 minute video by a great professor, director or actor (sometimes they are all three) than plough through a dense tome, however valid, written in dry academic jargon. &nbsp; These lecture series are not cheap, however (though in our opinion still worth the money). &nbsp; Ideally they are watched in a group setting with the cost shared. </p></li>
		<li><p><span class="blueprint">UTUBE</span> – Utube is a tremendously useful vehicle for finding Shakespeare gems which come from a wide range of sources. &nbsp; While some are amateur commentary (in the sense of being presented in a personal capacity rather than through a professional or commercial body) they are no worse for that. &nbsp; However, with so much on Utube you have to kiss a lot of frogs before you find a prince, making it time-consuming. &nbsp; When we stumble on good stuff we order it into our ongoing database. &nbsp; The net result is that it is quick and easy for Versebuster members to find stimulating videos by play or character, or just on performance and verse-speaking in general. </p></li>
		<li><p><span class="blueprint">FILMS & TV PRODUCTIONS</span> – outside a live performance there are few better and quicker ways to get the general idea of the themes and plots of a Shakespeare play than by watching a film or TV production of the same. &nbsp; We list by play and rate them for (1) watchability (2) verse-speaking (3) faithfulness to the text. &nbsp; Some are not faithful but watchable. &nbsp; Some are faithful but a dirge. &nbsp; Sometimes they are both faithful and watchable with the verse-speaking terrific to-boot. &nbsp; With our rating system you don’t need to waste three hours to discover you just watched a dud. </p></li>
		<li><p><span class="blueprint">AUDIO RECORDINGS</span> – the advantages of audio recordings is that there is total focus on the voice and productions tend to take less liberties with the text. &nbsp; They are ideal workshop and classroom aids, especially when there is more than one recording to compare styles of delivery. &nbsp; The voice of Paul Robeson will give you goosebumps; and what of the velvet delivery of John Geilgud? &nbsp; Has Richard Griffiths as Falstaff ever been bettered? &nbsp; Marvel at the virtuosity of Judi Dench and Edith Evans; and, while admiring the sheer depth of talent of the massed ranks of Shakespearean actors past and present, wince at the over-the-top performances, yawn at the under-energised, strain to the garbled, and groan at those that drone. &nbsp; A lot can be learned by observing others.</p></li>
	</ul>
<br>	
<p><strong>PART C – BEGINNERS’ BEST MATERIAL</strong></p>
	<ul start="9" style="list-style-type:roman; ">
		<li><p><span class="blueprint">BEGINNERS</span> – when it comes to Shakespeare keeping it short and simple for beginners is the best. &nbsp; Too much too soon can be very off-putting for most students. &nbsp; Nothing of course beats getting a beginner up on their feet and actually speaking some lines, but a brief and gentle grounding  in Elizabethan theatre practice  and verse ‘know-how’ will inspire and build confidence. &nbsp; These materials – whether books, articles, videos, films, lectures, etc., 	&#8210; offer that. &nbsp; They are our top picks for getting newbies into Shakespeare.</p></li>
	</ul>

