		<p>A SHAKESPEARE APPROACH FIT FOR THE 22ND CENTURY</p>

		<p>To push back against these tides of cultural erosion may appear as futile as the efforts of King Canute, but we should at least try. &nbsp;Otherwise our grandchildren, and their children and grandchildren, may be deprived of one the greatest art-forms ever devised in the history of humanity. &nbsp;And it is, of course, Shakespeare’s humanity that above all makes him fit for all mankind. &nbsp;As his rival and friend Ben Jonson put it:</p>

		<p class="text-center"><i>He was not of age, but for all time!</i></p>

		<p>One way to achieve this is to vigorously repackage Shakespeare performance technique in new and imaginative ways that appeal to the Facebook generation. &nbsp;In the TEFL industry (Teaching English as a Foreign Language) the better schools use a range of colourful, interactive, multi-media means to help students speak the language and make it a more enjoyable process. &nbsp;When people enjoy learning they learn faster. &nbsp;Why not the same with the language of Shakespeare? &nbsp;We have made a beginning, but it costs a lot of money. &nbsp;With proper funding we can devise new and better tools. &nbsp;Unfortunately the prospect of help from Arts Council England is poor and dwindling. &nbsp;Its one billion pound annual budget is now only funding <span class="blue">"ethnically diverse projects" <sup>7 & 8</sup></span>.	</p>

		<p>The diversity is not, however, as great as one would be led to believe. &nbsp;<span class="blue">Arts Council England funds the Muslim Festival of Cultures, but does not fund, say, a Christian Festival of Cultures, or a Jewish one <sup>9</sup></span>. &nbsp;Since this is taxpayers’ money and still a predominantly Christian country, dare one ask why? &nbsp;</p>


