		<p>This will not appease PC forces that believe Shakespeare is an irredeemable symbol of white male privilege regardless of how he is packaged or taught. &nbsp;Their guns on this issue may have been temporarily silenced, but they not gone away or given up; they are constantly regrouping as the incident this year at Yale bears witness to. </p>

		<br>

		<p><b>Part 2 </b></p>

		<p>The second storm-front takes a different form. &nbsp;<span class="blue">A recent report by the sociolinguistic department of the University of York <sup>6</sup></span> states that, with the great migration of peoples to America and Europe we see today, by 2066 the '[TH]' sound will be lost to London and other high migrant areas so many immigrants cannot manage it. &nbsp;Needless to say by the year 2100 or sooner that would have a devastating, if not fatal, impact on Shakespeare-speaking. &nbsp;In 1603, more or less the pinnacle of Shakespeare’s career, Sir Walter Raleigh was threatened by the Crown prosecutor with the words:</p>

		<p class="text-center">'<i>I thou thee, thou traitor! </i>'</p>

		<p>Difficult though they are sometimes to interpret, the importance of switches between the formal ‘you’ and the informal ‘thou’ forms cannot be overstated in Shakespeare performance. &nbsp;And yet there are opinion-formers on both sides of the Atlantic who feel that, if we must insist on Shakespeare, at the very least it should be re-written to exclude such archaic forms. &nbsp;They are seemingly ignorant of the vital contribution the ‘thou’ form makes to the status games of a Shakespeare plot. &nbsp;What is Shakespeare without such conscious manoeuvrings of status and the unconscious ones of Fortune’s fickle wheel?</p>

		<p>According to the conclusions of the report the best we could hope for by 2100 is '<i>I fou fee, fy traitor</i>'. &nbsp;It is an extraordinary thought that in 100 years perhaps the only counties where actors will still be able to enunciate Shakespeare will be in the barely-English speaking, low inward migration republics of India and the Philippines. &nbsp;Of those two, however, only India has a Shakespeare pedigree.</p>

