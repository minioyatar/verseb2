			<p>MORTAL THREATS TO SHAKESPEARE’S ART-FORM </p>

			<p>As the 400<sup>th</sup> commemoration of Shakespeare’s life and work draws to a close it would seem from all the publicity it engendered worldwide that his future at the heart of Western culture is secure and it would be preposterous to suggest otherwise. &nbsp;While we accept that Shakespeare as <i>literature</i> on the shelves of the Bodleian Library or the Folger Institute is forever assured, on the <i>performance</i> front we are not so confident on a 50-100 year projection. &nbsp;Two seismic cultural forces are at work. &nbsp;In relation to Shakespeare, neither is benign.</p>

			<br>
			<p>GATHERING STORM CLOUDS </p>

			<p><b>Part 1</b></p>

			<p>The first storm-front is gathering in academia and on campuses throughout the Anglosphere.  The dominant PC view now emerging is that a phenomenon such as Shakespeare is the oppressive and unfair product of historic white, male privilege.  Therefore, the argument runs, in the new global village where all cultures are 'equal', Shakespeare should be relegated in the public curriculum, or, better still, dropped altogether to give other cultures 'artistic breathing space'. &nbsp;This was the <span class="blue">argument advanced earlier in 2016 by Yale University students <sup>1</sup></span> and at <span class="blue">a school in California <sup>2</sup></span> (links at end). &nbsp;That this argument is gaining traction among young, non-Caucasians is certainly understandable - sixteenth Elizabethan / Jacobean England must seem very remote to those who are not its descendants. &nbsp;Two things alarm us though.</p>

			<p>First, the protest drew fairly wide support from members of the Yale faculty. &nbsp;There seems here a failure to grasp that Shakespeare's voice is universal because he articulates the human condition. &nbsp;The human condition is independent of colour, race, background, geography, sexuality or belief. &nbsp;At various points throughout our lives we all feel anger, jealousy, envy, revengeful, scorned, spurned, in love, out of love, unwanted, unappreciated, abused, ambitious, greedy, and so forth. &nbsp;Shakespeare's gift to mankind is that he wrote with dazzling psychological insight about imperfect people subjected to huge pressures in an imperfect world. &nbsp;All other claims about him are, in comparison, full of sound and fury and signifying nothing.</p>

			<p>Second, the view that Shakespeare is merely the product of unfair privilege is aggressively promoted by social justice warriors across the Anglosphere, many of whom are themselves white, rich and privileged. &nbsp;As Shakespeare was a rural grammar-school boy the irony cannot be missed! &nbsp;The essential flaw of the argument, it seems to us, is to judge the realities of pre-16<sup>th</sup> century England through the prism of contemporary preoccupations with diversity. &nbsp;</p>

			<p>In 1999 two-time Oscar winner Glenda Jackson, then an M.P, was <span class="blue">forced to defend Shakespeare in schools <sup>3</sup></span> against those in her own party, the Labour Government of the day, who saw Shakespeare as ripe for eradication from Britain’s national curriculum as he no longer suited their prescription for modern multicult Britain.</p>

			<p>The debate then evolved to how Shakespeare should be taught. &nbsp;In this respect we have some sympathy with those who argued, and continue to argue, that Shakespeare in schools is boring. &nbsp;Where we part company is that we don’t believe this need be so. &nbsp;What has been lacking all too often was, and still is, imagination in teaching it.</p>

			<p>To cut the story short, a consensus quickly emerged that giving ‘kidz’ dumbed down texts was the worst way, while getting children on their feet and speaking it was the best way. &nbsp;In his 2006 book <span class="blue"><i>Beyond Words: How Language Reveals the Way We Live Now</i> <sup>4</sup></span>, British broadcaster John Humphries cites this example of the former:</p>

