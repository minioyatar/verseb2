		<p>To avoid causing careless offence to any of the great variety of our readers, we make it abundantly clear that we have no problem at all with completely non-white, or female-only, or gay-only productions of Shakespeare. We’ve seen some in our time and a couple have been quite brilliant.</p>

		<p>By logical extension, nor do we have a problem with mixed race or gender-bending productions – indeed some of the Versebuster squad have been in these themselves.</p>

		<p>If we were casting our own production, our main criteria would be the ability of an actor to handle the verse and the antithetical prose, as well as the strength and clarity of their voice. &nbsp;Gender, sexuality or colour would not come into it. &nbsp;Nor would physical disability unless it placed impossible constraints on the part, or on the production as a whole.</p>

		<p>However, we believe that is a decision for producers and directors only and the government should not impose politically-motivated quotas. &nbsp;Let the theatre-going-public decide by voting their approval or disappointment through ticket-sales. &nbsp;Or is being suggested that, never again, can there ever be an all-white production of Shakespeare? &nbsp;That way tyranny lies – exactly the kind of thing Shakespeare articulated against.</p>
		
