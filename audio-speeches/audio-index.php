				<li><p>SOCIAL STATUS &nbsp;&nbsp; Initial status is fairly clear in most characters. &nbsp; However:</p>
					<ul class="decimal-list">
						<li><p>characters may change status during the course of the action (the Bastard in <span class="branding-style-title">King John</span>, Edgar in <span class="branding-style-title">King Lear</span>, Macbeth, Perdita in <span class="branding-style-title">The Winter's Tale</span>, etc.</p> </li>

						<li><p>sometimes status is very open to interpretation.&nbsp; Is the Jew, Tubal in <span class="branding-style-title">The Merchant of Venice</span> Shylock's social inferior or superior? &nbsp; Shakespeare doesn't say. &nbsp; But one way plays better than another. &nbsp; If you're familiar with the play, can you figure out which way that might be and why?</p> </li>
					</ul>
					<br>
					<li><p>RACE & REGION &nbsp;&nbsp; This category can be interesting for actors wishing to show their range by doing accents, or for directors looking for novel ways to cast. &nbsp; Again there is some subjectivity involved in our selections.</p></li> <br>
					<li><p>AGE &nbsp;&nbsp; It is not often is someone's specific age given - Juliet (just shy of 14) is one of the few, so mostly we suggest approximate age groupings. &nbsp; Keep in mind too Shakespeare paid lip service to biographical accuracy, so his historical characters are often loose interpretations of the real-life figures they are inspired by - age included. &nbsp; Hotspur and Prince Hal (the future Henry V) are made to be roughly the same age in <span class="branding-style-title">Henry IV Pt 1</span>, but in real life Hotspur was 23 years older. &nbsp; Sometimes there is no clue at all, the above mentioned Tubal being such an example. &nbsp; Still, we have fun evaluating certain characters, particularly women, where their age can be more critical to the dynamics of plot. &nbsp; Feel free to join the debate on the forum.</p></li>
				</li>
			</ul>
