			<p>VOCAL CONSIDERATIONS</p>		
			<ul>
				<li><p>
					VOLUME  &nbsp; For the most part vocal levels are not a problem, but there are times when it is difficult for us to judge the appropriate volume levels for a certain speech.  For instance, the famous choruses of <i><u>Henry V</u></i> could be spoken intimately or declaimed boldly. &nbsp;There isn't a right or a wrong. &nbsp;Performing live it depends on:</p>
			
			<ul class="decimal-list">
				<li><p>the size of the space</p> 

				<li><p>the context in the play (standing on a castle wall or tip-toeing around a lady's chamber while she sleeps)</p> 
					
				<li><p>artistic interpretation</p>
				</li>
			</ul>
				
				<p>So don't slavishly follow what we do. &nbsp;Play around and see what works for you. &nbsp;Being generally 'shouty' is not advised, however; the audience can't take it and it's the sort of thing that gives Shakespeare a bad name. &nbsp;Of course, shouting is fine when the context demands it. &nbsp;Some scenes, like the opening of the <i><u>The Tempest</u></i>, could hardly be done otherwise. &nbsp;In <i><u>Henry V</u></i>, Henry's 'St. Crispin's Day' battlecry might be another (only the last line, not the whole speech!).</p>
				<br>

				<li><p>POINTING UP SWITCHES BETWEEN 'YOU' & 'THOU' FORMS &nbsp; Since our aural medium does not allow us to physicalise such switches (by moving closer / further away, etc), we try to reflect them by adjusting vocal combinations of tone, volume, pitch and pace.</p> 
				
				<br>
				<li><p>ACCENT &nbsp; We are a predominantly British outfit so you get predominantly British accents (from mid-2017 forward we hope to have contributions from our American and Canadian cousins across the pond, as well as from other nationalities). &nbsp;However, Shakespeare works gloriously well in any accent so long as diction is audible and clear. &nbsp;Shakespeare was originally spoken in various accents that lay, for the most part, somewhere between West Country England and Dublin, Ireland. &nbsp;This is not Prince Charles or BBC English; it is not the British accent you will hear. &nbsp;These educated / metropolitan / upper-class accents are 'Received Pronunciation' (often referred to as 'RP' for short, or ‘Ultra RP’ in the case of Prince Charles) and arose from the House of Hanover taking over the English throne in the 18th century. &nbsp;This introduced a much flatter Germanic sound to the royal court which, over time, courtiers, unconsciously or otherwise, assimilated into their speech patterns.</p> 

					<p>The Northern Broadsides Theatre Company of Britain have a popular following because their muscular northern accents are noted for the way they reinvigorate Shakespeare's text and make it sound fresh. &nbsp;So wherever you are in the English speaking world, if you have a non-British RP accent, so long as you speak clearly and confidently, it could be an advantage. &nbsp;That said, in the age of equality, there is nothing wrong with 'posh' either. &nbsp;In the end it's the quality of interpretation and verse-speaking that count - the two are indivisible.</p>
