			<ul>
				<li>
					<p>we pronounce 'war' to rhyme with 'scar' as it did in Elizabethan times. &nbsp;This is to remind listeners Shakespeare heard something different to us today and we need to remain alert to this fact or we miss some telling rhymes (they are not  there just to be pretty). &nbsp;Please note this is the only concession we make to original pronunciation otherwise other verse-nurse points would be hard to convey. &nbsp;Professor David Crystal and his son Ben Crystal have done some fine workshops and performances on original pronunciation helping to popularise "OP" in the process. &nbsp;If you are interested in this aspect we highly recommend their books and YouTube listings. &nbsp;<i>See also ACCENT under VOCAL CONSIDERATIONS below. </i></p>
				</li>
				<li>
					<p>if the metre technically requires it we pronounce words at the end of the line that end in '<i>-ion</i>' or '<i>-ience</i>' or '<i>-ient</i>' with an extra syllable i.e. [<i>con-di-ti-on</i>], [<i>am-bi-ti-on</i>], [<i>im-pa-ti-ence</i>], etc. &nbsp;These are very common. &nbsp;In addition to these, there is the famous instance of [<i>o-ce-an</i>] in <i><u>Henry V</u></i>. &nbsp;In <i><u>Richard lll</u></i> examples would include [<i>Wood-e-ville</i>] for Woodville, <i>[He-ne-ry]</i> for Henry and [<i>Vaugh-an</i>] for Vaughan. &nbsp;It is not known for sure if Elizabethan actors added syllables to fill the metrical gap, but we illustrate the point anyway in the recordings.</p>
				</li>
				<li>
					<p>if the metre technically requires it we pronounce words with reverse stress i.e. [<i>al-LIES</i>] for 'allies', [<i>ro-YAL</i>] for 'royal', [<i>AD-ver-s'ry</i>] for 'ad-VER-sa-ry' (British pronunciation), etc. &nbsp;</p>
				</li>
				<li>
					<p>if the metre technically requires it we contract words i.e. [<i>VIRt-chus</i>] for 'virtuous', [<i>las-CIV-yous</i>] for 'lascivious', [<i>LOVE-ler</i>] for 'lovelier', etc. &nbsp;Again, we do not know for sure how far Elizabethans travelled in this direction, only that they contracted (elided) far more than 'educated' accents do today. &nbsp;Moreover, before 1600 - roughly the mid-point of Shakespeare's career - [T]s at the end of words were not sounded. &nbsp;Hence the actor playing Lady Anne in <i><u>Richard lll</u></i> might have said to Richard "<i>if thou deligh</i> [sic] <i>to view thy heinous deeds...</i>"</p>
