				<li><p>VOLUME (2)&nbsp;&nbsp; For advanced Shakespeare actors > pointing up switches been '<em>you</em>' 
					and '<em>thou</em>' (see <a href="">Blog</a>).</p> 

					<p>There is an argument that when you are speaking to someone and your character switches 
					mid-stream from addressing them with the intimate, informal '<em>thou</em>' to the public, 
					formal '<em>you</em>', one can (or should) physicalise this by increasing the distance from 
					the person to whom you are speaking (the rule here being that the person or persons being 
					spoken to don't move from their position(s); you can only move around when you speak).</p> 

					<p>The argument continues that one also increases volume so that so the person(s) you are 
					addressing can still hear you (logical) and, if others are present, so they too can here the  
					formal 'public' bits.</p>

					<p>Likewise on a reverse switch from '<em>you</em>' to '<em>thou</em>' the opposite holds,  
					moving closer to the person you're speaking to and lowering the volume and / or adopting a 
					more intimate tone.</p> 

					<p>Needless to say on the face of it this might seem a crude concept, but actually it can be very 
					effective.&nbsp; Done well, it reveals more psychological layers in a speech (or scene) and, 
					therefore, it will be of more interest to the audience. &nbsp; Accordingly, 
					we try to reflect it vocally in our recordings.</p> 

					<span class="branding-style-reg">NB</span>
					A switch to '<em>thou</em>' may also herald an insult, but an insult doesn't always need to be 
					bellowed; it could be hissed, spat, or whispered.</p>

					<p>For a more in-depth discussion see the post on <a href="">You v. Thou</a> in our Blog.</p>
