				<li><p>VOLUME (2)&nbsp;&nbsp; For advanced Shakespeare actors > pointing up switches been '<em>you</em>' 
					and '<em>thou</em>' (see <a href="">Blog</a>).</p> 

					<p>There is an argument that when you are speaking to someone and your character switches 
					mid-stream from addressing them with the intimate, informal '<em>thou</em>' to the public, 
					formal '<em>you</em>', one can (or should) physicalise this by increasing the distance from 
					the person to whom you are speaking (the proviso being that the person or persons being 
					spoken to don't move from their position(s); you can only move around when it’s your turn you speak).</p> 

					<p>Likewise on a reverse switch from '<em>you</em>' to '<em>thou</em>' the opposite holds – you  move closer to the person you're speaking to.</p> 

					<p>This might seem a crude concept, but, as workshops and performances have proved, it can actually be very effective. &nbsp; The alternative, which is not to acknowledge switches between you and thou, is the stuff of amateurs and Bard-lite. &nbsp; So somehow the switches need to be theatricalised for the audience - they cannot read your mind. &nbsp; Since recordings operate in an aural rather than a visual medium, we try to reflect these switches vocally by adjusting  combinations of tone, volume, pitch and pace.</p> 

					<span class="branding-style-reg">NB</span>
					<p>A switch to '<em>thou</em>' may also herald the start of heated argument and tit-fo-tat insults, but an insult doesn't always need to be bellowed; it could be hissed, spat, relished  or whispered.</p>

					<p>For a more in-depth discussion see the post on <a href="#">'You' v. 'Thou'</a> in our Blog.</p>
