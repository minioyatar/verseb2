			<p>DISCLAIMER</p>
			<p>The intention of the recordings is to illustrate the overall shape of a speech and particular aspects of its textual scoring (the notations you'll find in the ePlay). &nbsp;They are not finished performance pieces and you should under no circumstances simply ape them. &nbsp;Rather you try to understand the process behind our choices based on the clues shown in the ePlay, i.e. pointing up an antithesis, slowing down on a monosyllabic line, noting sarcasm or an interesting switch from 'you' to 'thou', taking advantage of a dramatic verse line ending, and so on.</p>
      <p>How much weight is given to each of those clues will naturally vary from actor to actor. &nbsp;Moreover, ambiguity of meaning and motive allow for different interpretations.</p>
      <p>Indeed, it would be possible to record a dozen different versions of speeches which are all perfectly valid providing the choices were based on the text and not imposed on it (i.e. the amateur's approach of saying "I think Hamlet's personality should be like this or this" and then smearing the text with this generalised jam regardless of textual clues that point to something far more subtle and interesting.) &nbsp;</p>
      <p>Simply put, the character is to be found in the text. &nbsp;If you can find a clue in the text that supports your choice you can do it. &nbsp;If you find clues that no one else has spotted, even better! &nbsp;Your work will be fresh and exciting. &nbsp;When someone gives a great performance it is because they dug deep into the text and exposed to the audience all manner of hidden treasure. &nbsp;</p>
      <p>So make the speeches your own. &nbsp; Although there are rules, there are also many choices for actors within the rules. &nbsp;The rules set actors free, while personal choices make each actor's performance unique.</p>
			
