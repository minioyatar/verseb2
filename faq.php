			    <p><strong>Why don't you use video for your audio speeches?</strong></p>
			    <p>EXPENSE</p>
			    <p>Expense is one issue.&nbsp; A video needs much more production-time and manpower - lighting, 
			    	wardrobe, make-up, props, etc.&nbsp; We operate on a tiny budget.&nbsp; On the other hand, 
			    	even if we had the money, when it comes to an oral tradition audio serves the purpose better 
			    	than video.&nbsp; Or perhaps we should say an aural tradition, as Elizabethans went to 
			    	'hear a play' not see one.</p>
			    
			    <p>PREJUDICE & PRECONCEIVED NOTIONS</p>

