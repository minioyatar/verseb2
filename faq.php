					<p>In addition, seeing faces is a distraction.&nbsp; It's bad enough we have prejudices 
						about voices, let alone our prejudices about how people look.&nbsp; This natural bias 
						that we all have against or in favour of a face is a distraction.&nbsp; Worse, 
						inexperienced actors may feel they have to ape the look, expressions and gestures.&nbsp;  It 
						also puts a preconceived notion into the head about how a character should look.&nbsp; This 
						can cause creative blocks since obviously we could only produce one look per speech. </p>
					
					<p>THE ISSUE OF SCALE</p>
					
					<p>The issue of scale also weighs.&nbsp; As every professional knows, performances have to 
						be scaled to fit the medium and space.&nbsp; Expansive gestures, bold facial expressions 
						and big vocal projection that might be desirable, even necessary, for cavernous theatres 
						and open-air theatre might look or sound ridiculous in intimate spaces.&nbsp; Again this 
						would present a production headache in video that is easier to resolve in audio. </p>
					
					<p>BLOCKING</p>
					
