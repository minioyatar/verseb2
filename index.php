			<div class="read-more-content">	

				<p>It does great harm to his plays and a major disservice to the audience if these are ignored. &nbsp;Many have written about these clues and the demands they make on speaking and performing Shakespeare. &nbsp;For the first time on the Internet, <span class="branding-style-reg">Versebuster<sup>&copy;</sup></span> brings it all together under one roof. &nbsp;Here are some of the clues and topics we cover (many overlap):</p>  

				<ul>
					<li><p>Prose v. verse</p>
						<ul style="margin-top: -10px;">
							<li><p>Differences in form / layout / architecture</p></li>
							<li style="margin-top: -10px;"><p>Disguise v. truthfulness and other implications</p></li>
						</ul>
					</li>
					<li style="margin-top: 10px;"><p>Different types of prose & verse</p></li>
					<li><p>Regular v. irregular verse</p></li>
					<li><p>Long lines (hexameters {Alexandrines}, heptameters, etc), short lines, half lines and shared lines</p></li>

					<li><p>Metre &amp; rhythm</p></li>

					<li><p>Elision (contraction) & expansion</p></li>
					<li><p>Verse line-endings &amp; mid-line endings</p></li>
					<li><p>End-stopped v. enjambement [or enjambment]</p></li>
					<li><p>Masculine line-endings v. feminine line-endings</p></li>
					<li><p>Masculine caesuras (caesurae) v. feminine caesuras (caesurae)</p></li>
					<li><p>Monosyllabic lines v. Latinate lines</p></li>
