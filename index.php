    										<p><span class="branding-style-reg">Which script to use.</span> &nbsp;It comes as a surprise to many people there is no settled and fixed text for many of Shakespeare’s plays. &nbsp;Part of the reason is that virtually nothing survives in the Bard’s own hand. &nbsp;Some argue the First Folio ('F1') of 1623 is the only true performance text, while others prefer an earlier Quarto for some 12 or so of his plays. &nbsp;For instance, for <i><u>Richard lll</u></i>, our inaugural play, expert opinion is divided whether Quarto 1 ('Q1') of 1597 should be deemed the true original performance text, or the F1 version which came seven years after Shakespeare's death in 1616. </p>
