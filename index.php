			<ul class="decimal-list">
				<li>
					<p><span class="branding-style-reg">Which script to use.</span> &nbsp; It comes as a surprise to 
						many people there is no settled and fixed text for many of Shakespeare’s plays. &nbsp; Part 
						of the reason is that virtually nothing survives in the Bard’s own hand. &nbsp; Some argue the 
						First Folio is the only true performance text, while others prefer a Quarto for some 12 of 
						his plays. &nbsp; The most commonly used script though, either through ignorance or choice, 
						is the modern ‘literary’ texts typically used in schools – Pelican, Penguin, Arden, New 
						Cambridge, Oxford, Riverside, etc – even No Fear has its advocates.</p>

					<p>Because we feel anyone serious about being good at Shakespeare should at least be familiar 
						with the arguments, Versebuster examines the merits of all three in our blogs. &nbsp; Moreover, 
						as we are a resource, we produce audio speeches, ePlays and scripts in all versions making 
						comparison easy.</p>
				</li>
				<li><p><span class="branding-style-reg">Line-endings in verse.</span> &nbsp; Some believe that verse-line
				 endings without punctuation nonetheless mark a caesura (a pause) and therefore invite a breath or at 
				 least a little lift at the end of the verse line before tumbling into the next. &nbsp; Others believe 
				 this is nonsense and, in essence, verse should be read as prose. &nbsp; We devote a lot of time to this 
				 issue. &nbsp; Where Versebuster itself stands on this is discussed in our <a href="">Blog</a>.</p></li>
