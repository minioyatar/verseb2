			<p>ANARCHY &amp; CREATIVITY</p>

			<p>There is a strand in contemporary thinking that believes 'rules' get in the way of originality 
				and the creative process. &nbsp;In recent times some have tried to do away with the 
				formalities of Shakespeare-speaking, in particular the architecture of verse, in pursuit of 
				rehearsal room spontaneity and 'keeping it real'. &nbsp;In each and every case this has proved 
				disastrous. &nbsp;Actors are unable to unleash the full potential of the text, and audiences 
				are left with Bard-lite and not a little boredom. &nbsp;Peter Hall (co-founder of the Royal 
				Shakespeare Company) and Giles Block (former ‘Master of the Words’ at Shakespeare’s Globe) 
				elaborate on this in their respective books <em>Playing Shakespeare</em> and <em>Speaking the Speech</em> 
				(see <a href="<?php echo SITE_URL_FILE; ?>eplay/bibliography.php">Bibliography</a>).</p> 

<!--START CALL OUT -->
		      <blockquote class="pull-quote">
		      	<p>"The skill, therefore, is not to ignore the rules or parameters, but to make them second nature so that you transcend them. &nbsp;Pushing against 'walls' produces great art, while anarchy leads to mediocrity"</p> 
		      </blockquote>
<!-- END OF CALL OUT -->

			<p>The truth is all great art forms, whether opera, comedy, painting, dance, etc., have parameters and rules. 
				&nbsp; These, in turn, give rise to the technique necessary to master them. &nbsp;The same can be said 
				of sport. &nbsp;Unlike sport though, the rules of engagement in art are hidden from public view, but 
				they are there and actually help the performer - just as they do in sport.</p> 

<p>Take, for instance, Olympic ice-skating. &nbsp;The stage is the rink and you may not skate outside this architectural parameter. &nbsp;You may not wear roller blades instead. &nbsp;You may not fall over without incurring penalties and certain moves are banned. &nbsp;You may not take forever to complete your routine - besides the official time limit the audience will get restless. &nbsp;Yet, despite these 'irksome' rules, Olympic ice-skaters repeatedly find ways to give sublime performances.</p>

<p>Moreover - to continue the analogy - they get marks for technique and marks for creative interpretation. &nbsp;The rub is that you cannot do a great interpretation without great technique - the very point emphasised by the scoring system. &nbsp;It is also true to say that technique alone is unlikely to secure you gold at this level of competition. &nbsp;There has to be some imagination and flair in their somewhere - some creative spark, some personal stamp. &nbsp;But fluid and seamless, not self-indulgent. </p>

			<p>The skill, therefore, is not to ignore the rules or parameters, but to make them second nature so that you 
				transcend them. &nbsp;Pushing against 'walls' produces great art, while anarchy leads to mediocrity.</p>

<br>
			<p>VERSEBUSTER'S 70% RULE - THE 'GET OUT OF JAIL' CARD</p>

			<p>For those who remain allergic to the notion of rules in Shakespeare we offer three comforts.</p> 

			<p>The first is that there is almost no facet of Shakespeare about which experts can be 100% absolutely 
				sure. &nbsp;Unless a personal diary emerges sensationally proven to be written in the Bard's own 
				hand filling the many gaps that still exist in our knowledge of the man and his methods, the best 
				anybody can say is that "such and such in Shakespeare generally holds true" and acknowledge there 
				are occasions it doesn't. &nbsp;For this reason Versebuster has framed two rules with regard to 
				Shakespeare performance - the 'Golden Rule' and the 'Seventy Per Cent Rule'. &nbsp;By far and 
				away most things fall under the Seventy Per Cent (70%) Rule.</p>

			<p>We call it the '70% Rule' because it trips off the tongue better than, say, the '82% Rule', 
				even though that may be nearer the truth. &nbsp;It means that the rule in question works very 
				well somewhere between 65% and 98% of the time. &nbsp;Experience and our guidance will tell you 
				towards which end of this spectrum a given rule leans.</p>

			<p>As to 'Golden Rules' these are rare, but there are a couple. &nbsp;One such is that, as John 
				Barton (RSC) says, you must always identify antitheses in Shakespeare's text and point them 
				up. &nbsp;Arguably this rule trumps any other rule.</p>

<br>
			<p>YOUR PERSONAL STAMP</p>

			<p>The second dollop of comfort is that rules don't negate the personal acting choices which make 
				your performance unique. &nbsp;For instance, a certain rule may say that when such and such is 
				encountered the actor must emphasise it. &nbsp;But HOW you emphasise is up to you. &nbsp;
				Typically one emphasises by increasing volume, but many interesting alternatives exist. &nbsp;
				You could, instead, alter tone, pace, pitch, or switch to sotto or a whisper.</p>

<br>
			<p>AMBIGUITY</p>

			<p>The final comfort that ensures no two performances in Shakespeare will be alike is ambiguity.</P>
						
			<p>When the Hostess recounts the death of Falstaff at the beginning of Act 2:3 in <span class="branding-style-title">Henry V</span> and relates that "<span class="branding-style-reg">he cried out 'God, God, God' three or four times"</span>, what tone does she assume? &nbsp; Is she inferring that Falstaff, in his final moments of senescence, is excitedly greeting God, or that he is in absolute terror of Him for a lifetime of thieving and whoring? &nbsp; Shakespeare provides no answer. &nbsp;So two (or more) perfectly valid, but diametrically opposed interpretations are possible which might substantially alter, if the actor were alive to the possibilities, their delivery of 'God, God, God'.</p> 

			<p>Within a Shakespeare play there might be a run of such ambiguities one scene after another and each time actors have to choose a plausible motivation, because, if not, performance and delivery lose focus and become generalised. &nbsp;Generalisation is theatrical boredom. &nbsp;So you have to commit. &nbsp; And the way you interpret (with or without the help of a director) will not be the same as the actor who played the same role last month. &nbsp;You are unique and this will bring, willy-nilly, a different perspective to the interpretation and a different way of dramatising it - gestures, voice, pace, expression and all. &nbsp; </p>

			<p>Whatever motivations actors commit to in these moments of ambiguity will 'ring true' to the audience so long as they can be justified by the text, and external stuff  (like a director's political agenda or an actor's personal hang-up) does not take precedence.</p>

<br>
			<p>CONCLUSION</p>

			<p>Don't imagine rules will cramp your style and individuality. &nbsp;Within the rules you have 
						creative choices. &nbsp;Trust in Shakespeare's architecture; it's there to help you.</p>
