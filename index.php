			<p><strong>Shakespeare's Performance Clues For Actors</strong></p>

			<p>Shakespeare directs and choreographs his plays through the myriad performance clues he embedded in the 
				text for the benefit of professional actors he knew well - line by line, phrase by phrase, word by word.</p>
