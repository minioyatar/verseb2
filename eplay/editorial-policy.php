<h3 class="page-title-ext text-center branding-style-reg">ePLAYS</h3>

			<p class="text-center"><strong>EDITORIAL POLICY</strong></p>
<br>
			<p>A LIGHT TOUCH</p>
			<p>As we produce ePlays and theatre scripts in individual Quarto, First Folio and modern text editions, we avoid excessive tampering with the Quartos and First Folio.   Remaining as faithful as is practicable to the original texts serves to highlight the differences with a modern literary or school version, one of our key aims. &nbsp; Secondly, we are less likely to inadvertently remove possible performance clues in the original texts not present in the modern ones. &nbsp; At the same time, however, we want them to be actable; so, like all editors, we are forced to make at least some changes.</p>  

			<p>This, of course, goes to the heart of the debate about editing Shakespeare. &nbsp; What 
				constitutes 'enough' changes? &nbsp; Below we list specifics, but, to give a general flavour, 
				we would follow all editors in changing the celebrated 'wh?ch' in F1 to 'which', but part 
				company by retaining the various name prefixes of Capulet's wife - mother, Lady, wife, etc. 
				&nbsp; At no point does Shakespeare call her 'Lady Capulet' and the textual evidence 
				supports the view the Capulets are nouveau riche not aristocrats - an important reflection 
				on the dynamics of the plot.</p>
<br>

			<p>COLOUR CODE</p>
			<p>To avoid confusion while boosting easy comparison between texts, we adopt a basic colour code  
				for F1 & F2 (Pericles only) and Modern. &nbsp; When using Modern, F1 & F2 references are <span class="txt-green">green</span>. 
				&nbsp; When using F1 or F2  Modern references are <span class="txt-magenta">magenta</span>. &nbsp; The colour code for Quartos 
				is <span class="txt-lightBlue">light blue</span>.</p> 
<br>
<br>

			<h4 class="text-center">SPECIFICS</h4>

			<p>MODERN TEXT<p>
			<p>Versebuster's modern text is drawn from a compilation of popular 'literary' editions - Arden, 
				Cambridge, Folgar, No Fear, Oxford, Penguin, Pelican & Riverside. &nbsp; However, what 
				makes our modern text edition different is that the annotations and notations focus solely 
				on performance concerns not literary ones. &nbsp; In other words, we take the ubiquitous 
				literary text but give it a performance makeover.</p> 

			<p>We do this because, such is the dominance of the literary text in the Shakespeare 
				education industry, literary texts remain the default scripts for performance; a dominance 
				compounded by widespread ignorance of, or indifference to the original 'performing' texts. &nbsp; 
				Even with the dogged efforts of F1 standard bearers like Patrick Tucker, Neil Freeman and, 
				more recently, Jonathan Bate of the RSC, that state of affairs is not going to change any time 
				soon. &nbsp; Therefore, our approach is to be pragmatic and offer a half-way house for the 
				'literary' majority.</p> 

<br>
			<p>FOLIO & QUARTO TEXT</p>
			<p>Our Folio and Quarto text (in modern type) aims to be a reasonably faithful representation 
				of the original. &nbsp; Such changes as we make are principally done to clear up obvious 
				mistakes and confusions. &nbsp; Elsewhere we suggest alternatives rather than impose.</p> 
<br>
			<p class="text-center"><span class="branding-style-reg"><strong>FIXES</strong></p> 
			<ul>
				<li>
					<p>OBVIOUS MISTAKES</p>
					<p>e.g. 'which' for 'wh?ch', 'unfortunate' for 'unforunate'</p> 
					<br>
				</li>
											
				
				<li>
					<p>CONFUSING SPELLING</p>
					<p>e.g.</p> 
					<p>'breathe' for 'breath' and vice-versa</p> 
					<p>'Ay' for 'I' when 'I' means 'yes' as opposed to the pronoun 'I'</p>
					<p>'than' for 'then', when the original 'then' is in the comparative sense</p>
<br>
				</li>

				<li>
					<p>PHONETIC SPELLING</p>
					<p>e.g. 'show' for 'shew'</p>
<br>
				</li>
				<li>
					<p>PUNCTUATION</p>
					<p>While we eschew the literary editors' traditional habit of changing colons to full-stops, 
						there are rare occasions we must follow suit. &nbsp; Other punctuation is changed only on a 
						'performance-need-to' basis. &nbsp; It is a matter of artistic judgement how far to go. &nbsp; 
						Not in a million years will it be possible to satisfy everybody, but we do keep an open mind
						and listen to feedback.</p> 
<br>
				</li>
			</ul>

			<p class="text-center"><span class="branding-style-reg">RETENTIONS</span>
			<ul>								
				<li>
					<p>LONG SPELLING</p>
					<p>'Heere' for 'here' and 'mee' for 'me' are retained, but where long spelling causes 
						confusion, e.g. 'breathe ' for 'breath', etc., it is changed.</p>
<br>
				</li>
													
															
				<li>
					<p>ORIGINAL SPELLINGS</p>
					<p>Distinctions between 'O' and 'Oh' retained</p>
<br>
				</li>

				<li>
					<p>F1 & F2 (<span class="branding-style-title">Pericles</span> only)</p>
					<p>Capitalisations and Ralph Crane parentheses retained</p>
<br>
				</li>
			</ul>

			<p class="text-center"><span class="branding-style-reg">SUGGESTIONS</p>
			<ul>
				<li>
					<p>DOUBTFUL WORDS</p>
					<p>'Odoration' - we suggest 'Adoration'</p>
					<p>'Dover Peer' - we suggest 'Hampton Peer (Southampton Pier)'</p>
<br>
				</li>
				<li>
					<p>DOUBTFUL PHRASES</p>
					<p>'Heare not my steps, which they may walke' - we suggest 'Heare not my steps, which 
						way they walk'</p>
					<p>'Alas, O frailtie is the cause, not wee, / For such as we are made, if such we 
						be' - we suggest 'Alas, our frailtie is the cause, not wee / For such as we be 
						made of, such we be'</p>
<br>
				</li>
			</ul>

			<p class="text-center"><span class="branding-style-reg">INSERTIONS</p>
			<ul>
				<li>
					<p>PUNCTUATION</p>
					<p>We try and leave original 'acoustic' punctuation alone as much as possible. &nbsp; 
						However, where we can clarify without 'harming', we do so; for instance, inserting 
						direct quotation marks or apostrophes that F1 typically leaves out.</p> 
<br>
				</li>
				<li>
					<p>MODERN ACT / SCENE DIVISIONS</p>
					<p>To make it easier for actors to compare F1 with a modern version we insert where 
						the modern act / scene divisions are but distinguish them from F1 by colour.</p> 
<br>
				</li>
				<li>
					<p>INCOMPLETE STAGE DIRECTIONS</p>
					<p>To make it easier for actors we inset modern amendments and additions without 
						removing the original and distinguishing from F1 by colour.</p>
<br>
				</li>
				<li>
					<p>MISSING ENTRANCES & EXITS</p>
					<p>To resolve F1's massed entrances, missing entries and exits we indicate modern 
						amendments and additions without removing the original and distinguishing from F1 
						by colour.</p>
				</li> 
			</ul>
