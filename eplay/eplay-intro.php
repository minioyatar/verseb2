	<h4 class="text-center">PRODUCING OUR EPLAYS - HOW WE ACHIEVE OUR GOALS</h4>
<br>
<br>
	
		    <p class="text-center"><strong>Versebuster annotations - radical departure (1)</strong></p>
		    
		    <p>ENTIRELY PERFORMANCE-ORIENTED NOTES</p>
		    <p>As Versebuster is focused solely on performance, our annotations and notations are entirely 
		    	performance-oriented. &nbsp; This is in stark contrast to most modern texts. &nbsp; Their notes  
		    	are predominantly literary as they are designed for schools and academia rather than for stage performance. &nbsp;  
		    	Historically their scholarship has made an invaluable contribution to the understanding of 
		    	intellectual sense (matter), but they fall well short on textual evidence related to mood,  
		    	attitude and sincerity (manner). &nbsp; These are the clues that help actors realise a far  
		    	more layered and nuanced performance; a performance closer to the Shakespeare's subtler  
		    	intentions and what seasoned audiences pay to see. &nbsp; Let's consider this issue in 
		    	more depth.</p>

			
			<p>A search on Google reveals 30 million hits for 'Shakespeare'. &nbsp; Of this, less than 0.1% 
				is actually about how to lift the words, especially his verse, off the page. &nbsp; A similar  
				pattern can be found in print. &nbsp; Lots of brilliantly clever people have written about   
				Shakespeare, but only a handful have focused on how to bring to life a heavily stylised art 
				form that has its origins in classical Roman and Greek rhetoric.</p>
	
			<p>Thus, as stated, Shakespeare plays are treated principally as a literary exercise, not a 
				performance one. &nbsp; Here is a an example taken from <em>Henry IV Pt 1</em>. &nbsp; 
				The prose line is:
<br>
			<div class="indentedverse">
				<blockquote>
					For though the camomile the more it is trodden the faster it grows, yet youth the 
					more it is wasted the sooner it wears.
				</blockquote>
			</div>
<br>
			Typically the only footnote to this sentence is:
<br>
<br>
			<p class="text-center"><strong>Camomile:</strong> a herb of the <em>aster</em> family
			
			<p>Given the context, knowing Camomile is a herb from the genus <em>aster</em> is a useless piece 
				of acting information. &nbsp; What editions should point out, but fail to, is that Falstaff's 
				line is classic Shakespearean antithesis:
<br>
			<div class="indentedverse">
				<blockquote>
					For though the CAMOMILE the more it is trodden the faster it GROWS, yet YOUTH the 
					more it is wasted the sooner it WEARS.
				</blockquote>
			</div>
<br>
			<p>As any experienced Shakespearean performer knows, it is the contrast of opposites (camomile v. 
				youth, grows v. wears) and the balancing halves of the sentence that must be pointed up in 
				the delivery, otherwise the audience loses the thread of the argument and possibly interest 
				in the actor. &nbsp; In this example what neither actor nor audience need is a lesson in 
				botany.</p>
<br>
		    <p>THOROUGH & COMPREHENSIVE</p>
