		    <p class="text-center"><strong>Three versions of each play - radical departure (4)</strong></p>
		    
		    <p>CHOICE OF FIRST FOLIO, GOOD QUARTO & MODERN
		    <p>Reflecting the different texts that directors may use in performance, each 
		    	ePlay comes in up to three versions. &nbsp; The different texts are the First 
		    	Folio (F1), a Good Quarto if it exists (such as Q2 for <span class="branding-style-title">
		    	Hamlet</span>) and a modern version. &nbsp; For a modern version we steer a middle 
				ground through Arden, Cambridge, Oxford, Penguin, Pelican, Folger and Riverside.</p>
<br>
<br>
<br>
		    <p>WHY WE OFFER THREE VERSIONS</p>
		    <p>Savvy Shakespeare actors know there are often dividends to be had from 
		    	comparing one text with another for the same play. &nbsp; This is because all three 
		    	texts are flawed and each has different pros and cons. 
<br>
<br>
		    <p><span class="branding-style-reg">F1 and Quartos</span></p>
		    <p>Broadly, the flaws are printing and textual errors. &nbsp; Examples:</p>
		    <ul>
				<li><p>Performance information might be incomplete or confusing - commonly exits
				 and entrances, other stage directions and line attribution</li>

				<li><p>Bits of text are 'missing' (whether accidental or deliberate cuts)</li>

				<li><p>Bits of text appear to be doggerel (whether real or imagined)</li>

				<li><p>Bits of punctuation appear muddled (whether real or imagined)</li>

				<li><p>Verse is set as prose and vice-versa due to 'printing problems' (whether real or imagined)</p>
			</li> 
			</ul>
<br>			
			<p><span class="branding-style-reg">A modern text</span></p>
			<p>Broadly, the flaws are dubious assumptions by editors (claim various critics). &nbsp; Examples:</p> 
			<ul>
				<li><p>Historically many editors have seen their job in terms of a literary duty 
				not a theatrical one; to serve the reader (often school teachers and their charges) not the 
				actor. &nbsp; Thus, over four centuries, they have amended text and punctuation to 'improve' 
				and clean up Shakespeare for a mass readership. &nbsp; In so doing, the argument runs, 
				important performance clues that would only be understood by actors 'in-the-know' have been 
				scrubbed out. &nbsp; An analogy often cited is that of an old Master painting that has been 
				over-restored, the layers, dirt and subtle hue of the original giving way to a sanitised 
				commercial product.</p>
				
				<li><p>The mental and creative processes of reading and performing are not the same. &nbsp; 
				When we read, our brains prefer short, neat sentences. &nbsp; When emotionally charged or filled  
				with passion, we abandon grammatical neatness. &nbsp; We speak in 'garden-path' sentences 
				that meander and zig-zag, justifying, emphasising, modifying, listing, repeating, accusing, 
				amplifying, exaggerating, mocking and so on. &nbsp; A solitary full stop may only come 
				when we've finally got it all off our chest!</p>

			<p>Now for Shakespeare's characters (as with any good drama) passion and pressure are a 
				permanent state of being. &nbsp; The characters, whatever their social status, combine an 
				Elizabethan love-affair with words with an overwhelming need to express themselves - to 
				muse, philosophise, share, reason, persuade, cajole, defend, criticise, argue, vent or lament. 
				&nbsp; And preferably this is done without the interruptions which full-stops are apt to 
				invite. &nbsp; It is argued that Shakespeare tried to capture the energy and excitement of 
				this verbal effusion through a preference for colons rather than full-stops (periods) and generally more fluid punctuation, and that 
				literary editors, unsuspecting of the theatrical damage, instead replaced these with clunking 
				full-stops to 'help' the reader. &nbsp; But, like obstructions on a runway, they prevent the 
				actor from taking off.</p>     
				
				<li><p>Appalled yet fascinated by personal mess and political chaos, Shakespeare writes about 
				imperfect people in an imperfect world. &nbsp; His characters often succumb to irrationality,
				poor judgement, confusion or madness. &nbsp; It is argued that Shakespeare sought to capture
				such moments by switching from regular 'correct' verse to irregular 'incorrect' verse. &nbsp;
				Literary editors have an instinctive aversion to poetic irregularity, overlooking its dramatic 
				possibilities on stage. &nbsp; Perhaps because they are not actors. &nbsp; 'All must be even
				in our government' is their mantra, and so they set about 'correcting' Shakespeare.</p>
			</li>
		    </ul>
<br>		    
		    <p>This is clumsy summation of a protracted debate so heated experts often come to blows (shouting 
		    	and finger-wagging at seminars is not unknown). &nbsp; We confidently predict it will never 
		    	be resolved, so we must live with the fact that all three texts - F1, Quarto and Modern - have 
		    	their admirers and their critics. &nbsp; But it's not a problem for inquisitive actors, 
		    	teachers and directors who will find extra character and performance information by judicious 
		    	cross-reference. &nbsp; As the old thespian adage goes, 'take the best and leave the rest'.</p>
		
		    <p>By putting all the texts into the same modern-type annotated format, Versebuster makes 
		    	comparison between them affordable and accessible to all.</p>
	    
