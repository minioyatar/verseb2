		    <p class="text-center"><strong>INTRODUCTION</strong></p>
  
<br>
	<p>CORE FUNCTION</p>
	<p>The core function of Versebuster's pdf ePlays is be an in-depth, performance-centred resource  
	for the actor, director or teacher whether using the First Folio (F1), a Good Quarto or a standard 
	modern text. &nbsp; Through annotation and notation they highlight as many performance clues as can be
	reasonably accommodated in one document. &nbsp; Those clues represent the distillation of knowledge
	about performing Shakespeare to be found in 20-odd self-help books by famous teachers and directors 
	listed in our bibliography.</p> 
	
<br>
	<p>SECOND FUNCTION</p>
	<p>Their second function (First Folio (F1) and Quarto only) is to show relatively unvarnished original scripts 
		(in modern type) so people get an idea of the challenges editors face. &nbsp; It is impossible 
		not to make some changes to an original script so as to make it actable. &nbsp; The on-going debate 
		revolves around how far an editor should go to achieve this. &nbsp; Many feel that until  
		comparatively recently editors were over zealous in making changes. &nbsp; In the latest editions there is a trend 
		back towards a lighter and defter touch, but still this fails to assuage some. &nbsp; As you 
		become familiar with the arguments on both sides, you'll be better able to judge yourself.</p>

<br>
	<p>THIRD FUNCTION</p>
	<p>Their third function (F1 and Quarto only) is to graphically show our 'before and after' changes. &nbsp; 
		Purists will be pleased we keep such changes to a minimum. &nbsp; For instance, a modern script may 
		make up to several thousand changes; our changes are in the low hundreds. &nbsp; In part we do this 
		to magnify the differences with a modern script; it makes comparison more interesting. &nbsp; If 
		you don't like our changes - either because you feel we changed too much or too little - we provide 
		a word.doc theatre script incorporating the ePlay changes which is yours to amend as you see fit.</p>
	
	<br>
	<p>FOURTH FUNCTION</p>
	<p>Their fourth function is to form the basis for our theatre scripts and audio speeches. &nbsp; Accordingly 
		the ePlays represent the forge and working-house of Versebuster thought. &nbsp; If we go wrong in the 
		audio or theatre script, we always go back to the ePlay to see where we went astray or missed something. &nbsp; 
		Thus, bit by bit, our product will evolve and improve over time. &nbsp; Constructive feedback is always
		welcome. &nbsp; However, you might want to check FAQs why we cannot, or chose not to do certain  
		things at this time (budget considerations, technical challenges, manpower, etc.).</p>  
	
